class Solution{

	public:
	int findSwapValues(int A[], int n, int B[], int m)
	{
        sort(A, A+n);
        sort(B, B+m);
        map<pair<int,int>, int> m;
        int i = 0;
        while(n--||m--)
        {
            m.insert(make_pair(A[i], B[i]), abs(A[i]-B[i]));
        }
	}
 

};

